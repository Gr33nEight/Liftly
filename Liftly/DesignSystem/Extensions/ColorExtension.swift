import SwiftUI

extension Color {
    enum custom {
        static let background = Color("CustomBackground")
        static let text = Color("CustomText")
        static let primary = Color("CustomPrimary")
        static let secondary = Color("CustomSecondary")
        static let tertiary = Color("CustomTertiary")
    }
}
